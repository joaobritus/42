int	ft_is_prime(int nb)
{
	int i;
	
	if (nb <= 1)
		return (0);
	i = 5;
	if (nb == 2 || nb == 3)
	{
		return (1);
	}
	if (nb % 2 == 0 || nb % 3 == 0)
	{
		return (0);
	}
	while (i <= (nb / 3))
	{
		
		if (nb % i == 0)
		{
			return (0);
		}
		i+=2;
	}
	return (1);
}

#include <stdio.h>

int	main(void)
{
	printf("%d", ft_is_prime(2147483647));
}
