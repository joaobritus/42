#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int z;

	i = 1;
	while (i < argc)
	{
		z = 0;
		while (argv[i][z] != '\0')
		{
			write(1, &argv[i][z], 1);
			z++;
		}
		write(1, "\n", 1);
		i++;
	}
	return(0);
}
