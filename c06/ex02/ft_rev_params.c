#include <stdio.h>

int	main(int argc, char **argv)
{
	int	i;

	i = 0;
	while (argv[i])
	{
		i++;
	}
	i--;
	while (i > 0)
	{
		printf("%s\n", argv[i]);
		i--;
	}
}
